void InitBoard(char board[ROW][COL],int row,int col)
{
	int i = 0;
	int j = 0;
	for(i = 0;i < ROW;i++)
	{
		for(j = 0;j < col;j++)
		{
			board[i][j] = ' '; 
		}
	}
}
void DisplayBoard(char board[ROW][COL], int row,int col)
{
	int i = 0;
	for(i = 0;i < row;i++)
	{
		int j = 0;
		for (j = 0; j < col; j++)
		{
			//1、打印一行的数据
			printf(" %c ",board[i][j]);
			if (j < col -1)
				printf("|");
		}
		printf("\n");
		//2、打印份隔行
		if(i < row - 1)
		{
			for (j = 0;j < col; j++)
			{
				printf("---");
				if (j < col - 1)
					printf("|");
			}
			printf("\n");
		}
	}
}


void PlayerMove(char board[ROW][COL],int row,int col)
{
	int x = 0;
	int y = 0;
	printf("玩家走：>\n");
	while (1)
	{
		printf("请输入要下的坐标：");
		scanf("%d %d",&x,&y);
		//判断x y的合法性
		if (x >= 1 && x <= row && y >= 1 && y<= col)
		{
			if (board[x - 1][y - 1] == ' ')
			{
				board[x - 1][y - 1] = '*';
				break;
			}
			else
			{
				printf("该坐标被占用\n");
			}
		}
		else
		{
			printf("坐标非法，请重新输入！\n");
		}
	}
}
void ComputerMove(char board[ROW][COL],int row,int col)
{
	int x = 0;
	int y = 0;
	printf("电脑走：>\n");
	while (1)
	{
		x = rand()%row;
		y = rand()%col;
		if (board[x][y] == ' ')
		{
			board[x][y] = '#';
			break;
		}
	}
}
//返回1表示棋盘满了，返回0表示棋盘没满
int IsFull(char board[ROW][COL],int row,int col)
{
	int i = 0;
	int j = 0;
	for (i = 0; i < row; i++)
	{
		for (j = 0; j < col; j++)
		{
			if (board[i][j] == ' ') 
			{
				return 0;//没满
			}
		}
	}
	return 1;//满了
}

char IsWin(char board[ROW][COL],int row,int col)
{
	int i = 0;
	int j = 0;
	//判断横行
	for(i = 0;i < row;i++)
	{
		for (j = 0; j < col - 2; j++)
		{
			if (board[i][j] == board[i][j + 1] && board[i][j + 1] == board[i][j + 2] && board[i][j] != ' ')
			{
				return board[i][j];
			}
		}
	}
	//判断竖列
	for (i = 0; i < row - 2; i++)
	{
			for (j = 0; j < col; j++)
		{
			if (board[i][j] == board[i + 1][j] && board[i + 1][j] == board[i + 2][j] && board[i][j] != ' ')
			{
				return board[i][j];
			}
		}	
	}
	//两个对角线
		for (i = 0; i < row - 2; i++)
	{
			for (j = 0; j < col - 2; j++)
		{
			if (board[i][j] == board[i + 1][j + 1] && board[i + 1][j + 1] == board[i + 2][j + 2] && board[i][j] != ' ')
			{
				return board[i][j];
			}
			else if (board[i +2][j] == board[i + 1][j + 1] && board[i + 1][j + 1] == board[i][j + 2] && board[i +1 ][j + 1] != ' ')
			{
				return board[i + 1][j + 1];
			}
		}	
	}
	//判断是否平局
	if (1 == IsFull(board,ROW,COL))
	{
		return 'Q';
	}
	return 'C';
}
