#include<stdio.h>
int main()
{
	int i = 0;
	int j = 0;
	for(i=1;i<10;i++)//ij
	{
		for(j=1;j<=i;j++)
		{
			printf("%d*%d=%2d",i,j,i*j);//%2d打印两个位置向右对齐
		}
		printf("\n");

	}
	return 0;
}
