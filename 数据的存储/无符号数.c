	//11111111111111111111111101111111
	//11111111111111111111111110000000——补码
	//10000000——要打印先提升
	//11111111111111111111111110000000——整型提升补码
	//打印十进制的无符号数
	printf("%u",a);
	//%d—打印十进制的有符号数
	//%u—打印十进制的无符号数
	return 0;
