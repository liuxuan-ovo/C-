//1
#include <stdio.h>
int main()
{
	char a = -1;
	//100000000000000000000000000000001
	//111111111111111111111111111111110
	//111111111111111111111111111111111——补码
	//11111111
	//111111111111111111111111111111111——整型提升
	//-1
	signed char b = -1;
	//11111111
	//-1
	unsigned char c = -1;
	//111111111
	//000000000000000000000000011111111
	//255
	printf("a = %d,b = %d,c = %d",a,b,c);
	return 0;
}
//-1  -1  255

