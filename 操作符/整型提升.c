#include<stdio.h>
int main()
{
	char a = 0xb6;		//10110110				11111111111111111111111110110110
	short b = 0xb600;	//1011011000000000		11111111111111111011011000000000	
	int c = 0xb6000000;
	if (a = 0xb6)//11111111111111111111111110110110
	{
		printf("a");
	}
	if (b = 0xb600)//11111111111111111011011000000000
	{
		printf("b");
	}
	if (c = 0xb6000000)
	{
		printf("c");
	}
	return 0;
}

//实例2
#include<stdio.h>
int main()
{
	char c = 1;
	printf("%u\n",sizeof(c));//1
	printf("%u\n",sizeof(+c));//4——c参与运算，发生整型提升
	printf("%u\n",sizeof(!c));//1
	return 0;
}
